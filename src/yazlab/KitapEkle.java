    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String KitapAdı = jTextField1.getText();
        String Yazar = jTextField2.getText();
        String Yıl = jTextField3.getText();
        String Yayın = jTextField7.getText();
        String URLL = jTextField4.getText();
        String URLM = jTextField5.getText();
        String URLS = jTextField6.getText();
        String ISBN = jTextField8.getText();
        String bos = "";

        if (!KitapAdı.equals(bos) && !Yazar.equals(bos) && !Yıl.equals(bos) && !URLL.equals(bos) && !URLM.equals(bos) && !URLS.equals(bos) && !Yayın.equals(bos) && !ISBN.equals(bos)) {
            try {
                Statement ac = (Statement) conn.createStatement();
                String sq2 = "SELECT ISBN FROM books";
                ResultSet sonuc2 = ac.executeQuery(sq2);
                ArrayList<String> kitapid = new ArrayList<>();
                while (sonuc2.next()) {
                    kitapid.add(sonuc2.getString("ISBN"));
                }
                if (kitapid.contains(ISBN)) {
                    jLabel8.setText("Veri Tabanında Aynı Kitaptan Var.");
                } else {
                    int ilanID = 1;
                    String sq = "SELECT COUNT(*) FROM books";
                    ResultSet sonucID = ac.executeQuery(sq);
                    while (sonucID.next()) {
                        ilanID = sonucID.getInt(1);
                    }
                    String sq1 = "INSERT INTO books VALUES('" + ISBN + "','" + KitapAdı + "','" + Yazar + "','" + Yıl + "','" + Yayın + "','" + URLS + "','" + URLM + "','" + URLL + "','" + ilanID + "')";
                    ac.executeUpdate(sq1);
                    conn.close();
                    new KitapEkle().setVisible(true);
                    dispose();
                }
            } catch (SQLException ex) {
                Logger.getLogger(KitapEkle.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            jLabel8.setText("Alanları Eksiksiz Bir Biçimde Doldurunuz.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

